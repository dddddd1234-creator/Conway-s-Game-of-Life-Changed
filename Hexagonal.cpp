int even_count(int x,int y) {
    int res=0;
    if (x>0&&y>0)res+=map1[x-1][y-1];
    if (x>0)res+=map1[x-1][y];
    if (y<29)res+=map1[x][y+1];
    if (x<29)res+=map1[x+1][y];
    if (x<29&&y>0)res+=map1[x+1][y-1];
    if (y>0)res+=map1[x][y-1];
    return res;
};
int odd_count(int x,int y) {
    int res=0;
    if (x>0)res+=map1[x-1][y];
    if (x>0&&y<29)res+=map1[x-1][y+1];
    if (y<29)res+=map1[x][y+1];
    if (x<29&&y<29)res+=map1[x+1][y+1];
    if (x<29)res+=map1[x+1][y];
    if (y>0)res+=map1[x][y-1];
    return res;
};
void makechange() {
    for(int i=0;i<30;i++) {
        for(int j=0;j<30;j++) {
            int next;
            if (i%2==0) {
                next=even_count(i,j);
            }
            else {
                next=odd_count(i,j);
            }
            if (next==2)mapclone[i][j]=1;
            else mapclone[i][j]=0;
        }
    }
    for(int i=0;i<30;i++) {
        for(int j=0;j<30;j++) {
            map1[i][j]=mapclone[i][j];
        }
    }
}
